package com.mks.jmx;

public class MBeanOperationException extends RuntimeException {

	private static final long serialVersionUID = -7322890367163093493L;

	public MBeanOperationException() {
		super();
	}

	public MBeanOperationException(String message, Throwable cause) {
		super(message, cause);
	}

	public MBeanOperationException(String message) {
		super(message);
	}

	public MBeanOperationException(Throwable cause) {
		super(cause);
	}
}
