package com.mks.codec.byteorder;

public class NumberCodecUtils {

	public static NumberCodec buildBigEndianNumberCodec() {
		return NumberCodecFactory.fetchNumberCodec(ByteOrder.bigEndian);
	}

	public static NumberCodec buildLittleEndianNumberCodec() {
		return NumberCodecFactory.fetchNumberCodec(ByteOrder.littleEndian);
	}

}
